ipcMain.handle('check-docker', async () => {
  try {
    await docker.ping();
    const info = await docker.info();
    return { 
      installed: true, 
      version: info.ServerVersion,
      running: true 
    };
  } catch (error) {
    return { 
      installed: false, 
      error: 'Docker is not installed or not running' 
    };
  }
});

ipcMain.handle('deploy-docker', async (event, config) => {
  try {
    const installPath = getInstallPath(config.deployment);
    
    // Send progress updates
    const sendProgress = (step, status, message) => {
      event.sender.send('install-progress', { step, status, message });
    };

    // Step 1: Pull images
    sendProgress('pull', 'running', 'Pulling Docker images...');
    await pullDockerImages(sendProgress);

    // Step 2: Create network
    sendProgress('network', 'running', 'Creating Docker network...');
    await createDockerNetwork('eastbooks-network');

    // Step 3: Start services
    sendProgress('services', 'running', 'Starting services...');
    await startDockerCompose(installPath, sendProgress);

    // Step 4: Wait for health checks
    sendProgress('health', 'running', 'Waiting for services to be healthy...');
    await waitForHealthy(config);

    // Step 5: Run database migrations
    sendProgress('migrate', 'running', 'Running database migrations...');
    await runMigrations(installPath);

    // Step 6: Seed initial data
    if (config.seedData) {
      sendProgress('seed', 'running', 'Seeding initial data...');
      await seedDatabase(installPath, config);
    }

    sendProgress('complete', 'success', 'Installation complete!');

    return { 
      success: true, 
      url: `http://localhost:${config.port || 3000}`,
      credentials: {
        email: 'admin@yourcompany.com',
        password: 'Check your email'
      }
    };
  } catch (error) {
    event.sender.send('install-progress', { 
      step: 'error', 
      status: 'error', 
      message: error.message 
    });
    return { success: false, error: error.message };
  }
});

