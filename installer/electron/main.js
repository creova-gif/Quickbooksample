// ========================================
// DOCKER COMPOSE GENERATION
// ========================================

ipcMain.handle('generate-docker-compose', async (event, config) => {
  try {
    let composeTemplate;

    switch (config.deployment) {
      case 'on-premise':
        composeTemplate = getOnPremiseCompose(config);
        break;
      case 'private':
        composeTemplate = getPrivateCloudCompose(config);
        break;
      case 'cloud':
        composeTemplate = getCloudCompose(config);
        break;
      default:
        throw new Error('Invalid deployment type');
    }

    const installPath = getInstallPath(config.deployment);
    await fs.writeFile(
      path.join(installPath, 'docker-compose.yml'),
      composeTemplate
    );

    return { success: true, path: installPath };
  } catch (error) {
    return { success: false, error: error.message };
  }
});

