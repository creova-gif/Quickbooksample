function getPrivateCloudCompose(config) {
  // Similar to on-premise but with external network and monitoring
  return getOnPremiseCompose(config) + `
  redis:
    image: redis:7-alpine
    container_name: eastbooks-cache
    networks:
      - eastbooks-network
    restart: unless-stopped
`;
}

function getCloudCompose(config) {
  // Minimal compose for cloud-managed database
  return getOnPremiseCompose(config);
}

async function pullDockerImages(sendProgress) {
  const images = [
    'postgres:15-alpine',
    'eastbooks/backend:latest',
    'eastbooks/frontend:latest'
  ];

  for (const image of images) {
    sendProgress('pull', 'running', `Pulling ${image}...`);
    
    await new Promise((resolve, reject) => {
      docker.pull(image, (err, stream) => {
        if (err) return reject(err);
        
        docker.modem.followProgress(stream, (err, output) => {
          if (err) return reject(err);
          resolve(output);
        });
      });
    });
  }
}

async function createDockerNetwork(name) {
  try {
    const networks = await docker.listNetworks();
    const exists = networks.find(n => n.Name === name);
    
    if (!exists) {
      await docker.createNetwork({ Name: name });
    }
  } catch (error) {
    console.error('Network creation error:', error);
  }
}

async function startDockerCompose(installPath, sendProgress) {
  return new Promise((resolve, reject) => {
    const process = spawn('docker-compose', ['up', '-d'], {
      cwd: installPath,
      stdio: ['ignore', 'pipe', 'pipe']
    });

    process.stdout.on('data', (data) => {
      sendProgress('services', 'running', data.toString().trim());
    });

    process.stderr.on('data', (data) => {
      sendProgress('services', 'running', data.toString().trim());
    });

    process.on('close', (code) => {
      if (code === 0) {
        resolve();
      } else {
        reject(new Error(`Docker Compose failed with code ${code}`));
      }
    });
  });
}

async function waitForHealthy(config, timeout = 120000) {
  const startTime = Date.now();
  const checkInterval = 5000;

  while (Date.now() - startTime < timeout) {
    try {
      const response = await fetch(`http://localhost:${config.port || 3000}/api/health`);
      if (response.ok) {
        return true;
      }
    } catch (error) {
      // Service not ready yet
    }
    
    await new Promise(resolve => setTimeout(resolve, checkInterval));
  }

  throw new Error('Services failed to become healthy');
}

async function runMigrations(installPath) {
  return new Promise((resolve, reject) => {
    const process = spawn('docker-compose', ['exec', '-T', 'backend', 'npm', 'run', 'migrate'], {
      cwd: installPath
    });

    process.on('close', (code) => {
      if (code === 0) {
        resolve();
      } else {
        reject(new Error('Database migration failed'));
      }
    });
  });
}

async function seedDatabase(installPath, config) {
  return new Promise((resolve, reject) => {
    const process = spawn('docker-compose', ['exec', '-T', 'backend', 'npm', 'run', 'seed'], {
      cwd: installPath
    });

    process.on('close', (code) => {
      if (code === 0) {
        resolve();
      } else {
        reject(new Error('Database seeding failed'));
      }
    });
  });
}

