ipcMain.handle('validate-license', async (event, licenseKey) => {
  try {
    // In production, this calls your license server
    // For now, decode and validate JWT structure
    
    const decoded = jwt.decode(licenseKey);
    
    if (!decoded) {
      return { valid: false, error: 'Invalid license format' };
    }

    // Validate expiry
    const now = Math.floor(Date.now() / 1000);
    if (decoded.exp && decoded.exp < now) {
      return { valid: false, error: 'License expired' };
    }

    // Validate issuer
    if (decoded.iss !== 'eastbooks.com') {
      return { valid: false, error: 'Invalid license issuer' };
    }

    return {
      valid: true,
      license: {
        plan: decoded.plan,
        deployment: decoded.deployment,
        users: decoded.users,
        modules: decoded.modules,
        expires: new Date(decoded.exp * 1000).toISOString().split('T')[0],
        support: decoded.support
      }
    };
  } catch (error) {
    return { valid: false, error: error.message };
  }
});

